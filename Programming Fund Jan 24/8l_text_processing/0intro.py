a = 'aSd'
b = a.swapcase()
print(b)            # AsD
c = a.capitalize()
print(c)            # Asd
d = a.upper()
print(d)            # ASD
e = a.lower()
print(e)            # asd
f = a.find('S')
print(f)            # 1

